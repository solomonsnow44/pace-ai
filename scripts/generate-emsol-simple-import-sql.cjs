const fs = require("fs");
const path = require("path");
const XLSX = require("xlsx");

const rootDir = path.resolve(__dirname, "..");
const campaignsDir = path.join(rootDir, "sampleCampaigns");
const outputPath = path.join(rootDir, "supabase", "emsol_simple_import.sql");

const files = [
  { file: "NHS Trust - Contacts - Last view used.xlsx", campaignName: "NHS Trust - Contacts" },
  { file: "NHS Webinar - Account Priority .xlsx", campaignName: "NHS Webinar" },
  { file: "Construction - Contacts - Last view used.xlsx", campaignName: "Construction - Contacts" },
];

function clean(value) {
  if (value === null || value === undefined) return null;
  const text = String(value).replace(/\u00a0/g, " ").trim();
  return text.length ? text : null;
}

function normalizeRow(row) {
  return Object.fromEntries(
    Object.entries(row)
      .map(([key, value]) => [clean(key), clean(value)])
      .filter(([key, value]) => key && value !== null),
  );
}

function sqlJson(value) {
  return `'${JSON.stringify(value).replace(/'/g, "''")}'::jsonb`;
}

const rows = [];

for (const config of files) {
  const workbookPath = path.join(campaignsDir, config.file);
  const workbook = XLSX.readFile(workbookPath, { cellDates: false });

  for (const sheetName of workbook.SheetNames) {
    const sheetRows = XLSX.utils.sheet_to_json(workbook.Sheets[sheetName], { defval: null, raw: false });
    sheetRows.forEach((row, index) => {
      const payload = normalizeRow(row);
      if (!payload["Organisation Name"]) return;
      rows.push({
        campaign_name: config.campaignName,
        source_file: config.file,
        source_sheet: sheetName,
        source_row: index + 2,
        payload,
      });
    });
  }
}

const sql = `-- Generated by scripts/generate-emsol-simple-import-sql.cjs.
-- Imports EMSOL workbook data into the simple CRM schema:
-- clients, campaigns, companies, contacts, campaign_targets.
-- Run after supabase/migrations/018_rebuild_simple_crm_schema.sql.

do $$
declare
  v_rows jsonb := ${sqlJson(rows)};
  v_org_id uuid;
  v_client_id uuid;
  v_campaign_id uuid;
  v_company_id uuid;
  v_contact_id uuid;
  v_company_name text;
  v_website text;
  v_domain text;
  v_industry text;
  v_employee_count integer;
  v_revenue numeric(18, 2);
  v_company_fields jsonb;
  v_target_metadata jsonb;
  v_rank integer;
  v_first_name text;
  v_last_name text;
  v_full_name text;
  v_job_title text;
  v_linkedin text;
  v_mobile text;
  v_direct_dial text;
  v_phone text;
  v_email text;
  v_location text;
  v_identity_key text;
  r record;
begin
  insert into public.organizations (name, slug, status, metadata)
  values ('PaceOps', 'paceops', 'active', jsonb_build_object('company_workspace', 'paceops'))
  on conflict (slug) do update
    set metadata = public.organizations.metadata || excluded.metadata,
        updated_at = now()
  returning id into v_org_id;

  insert into public.clients (organization_id, name, slug, status, metadata)
  values (v_org_id, 'EMSOL', 'emsol', 'active', jsonb_build_object('source', 'sampleCampaigns'))
  on conflict (organization_id, slug) do update
    set name = excluded.name,
        status = 'active',
        metadata = public.clients.metadata || excluded.metadata,
        updated_at = now()
  returning id into v_client_id;

  for r in
    select distinct campaign_name
    from jsonb_to_recordset(v_rows) as x(
      campaign_name text,
      source_file text,
      source_sheet text,
      source_row integer,
      payload jsonb
    )
    order by campaign_name
  loop
    insert into public.campaigns (organization_id, client_id, name, status, channel, settings)
    values (
      v_org_id,
      v_client_id,
      r.campaign_name,
      'active',
      'Outbound',
      jsonb_build_object('source', 'sampleCampaigns')
    )
    on conflict (client_id, name) do update
      set status = excluded.status,
          channel = excluded.channel,
          settings = public.campaigns.settings || excluded.settings,
          updated_at = now();
  end loop;

  for r in
    select *
    from jsonb_to_recordset(v_rows) as x(
      campaign_name text,
      source_file text,
      source_sheet text,
      source_row integer,
      payload jsonb
    )
    order by campaign_name, source_row
  loop
    v_company_name := nullif(trim(r.payload ->> 'Organisation Name'), '');
    if v_company_name is null then
      continue;
    end if;

    v_website := nullif(trim(coalesce(r.payload ->> 'Website', '')), '');
    v_domain := case
      when v_website is null then null
      else lower(regexp_replace(regexp_replace(regexp_replace(v_website, '^https?://', '', 'i'), '^www\\\\.', '', 'i'), '/.*$', ''))
    end;
    v_industry := nullif(trim(coalesce(r.payload ->> 'Industry', '')), '');
    v_employee_count := case
      when nullif(regexp_replace(coalesce(r.payload ->> 'Org. Size', ''), '[^0-9]', '', 'g'), '') is null then null
      else regexp_replace(coalesce(r.payload ->> 'Org. Size', ''), '[^0-9]', '', 'g')::integer
    end;
    v_revenue := case
      when nullif(regexp_replace(coalesce(r.payload ->> 'Turnover (£) - amount', ''), '[^0-9.]', '', 'g'), '') is null then null
      else regexp_replace(coalesce(r.payload ->> 'Turnover (£) - amount', ''), '[^0-9.]', '', 'g')::numeric(18, 2)
    end;

    v_company_fields := jsonb_strip_nulls(jsonb_build_object(
      'org_linkedin', r.payload ->> 'Org. LinkedIn',
      'ods_code', r.payload ->> 'ODS Code',
      'turnover_currency', r.payload ->> 'Turnover (£) - currency',
      'company_hq_location', r.payload ->> 'Company HQ Location',
      'other_locations', r.payload ->> 'Other Locations'
    ));

    select id into v_company_id
    from public.companies
    where organization_id = v_org_id
      and (
        (v_domain is not null and lower(domain) = lower(v_domain))
        or lower(name) = lower(v_company_name)
      )
    order by
      case when v_domain is not null and lower(domain) = lower(v_domain) then 0 else 1 end,
      created_at
    limit 1;

    if v_company_id is null then
      insert into public.companies (
        organization_id,
        client_id,
        name,
        slug,
        domain,
        website,
        industry,
        employee_count,
        annual_revenue,
        status,
        custom_fields
      ) values (
        v_org_id,
        v_client_id,
        v_company_name,
        lower(regexp_replace(regexp_replace(trim(v_company_name), '[^a-zA-Z0-9]+', '-', 'g'), '(^-|-$)', '', 'g')),
        v_domain,
        v_website,
        v_industry,
        v_employee_count,
        v_revenue,
        'active',
        v_company_fields
      )
      returning id into v_company_id;
    else
      update public.companies
      set client_id = coalesce(public.companies.client_id, v_client_id),
          domain = coalesce(public.companies.domain, v_domain),
          website = coalesce(public.companies.website, v_website),
          industry = coalesce(public.companies.industry, v_industry),
          employee_count = coalesce(public.companies.employee_count, v_employee_count),
          annual_revenue = coalesce(public.companies.annual_revenue, v_revenue),
          custom_fields = public.companies.custom_fields || v_company_fields,
          updated_at = now()
      where id = v_company_id;
    end if;

    select id into v_campaign_id
    from public.campaigns
    where client_id = v_client_id
      and name = r.campaign_name;

    v_target_metadata := jsonb_strip_nulls(jsonb_build_object(
      'account_priority', r.payload ->> 'Account Priority',
      'account_notes', r.payload ->> 'Company Description',
      'technology_stack_notes', r.payload ->> 'Technology Stack',
      'prospecting', r.payload ->> 'Prospecting',
      'discovery', r.payload ->> 'Discovery',
      'closing', r.payload ->> 'Closing',
      'progress', r.payload ->> 'Progress',
      'appointment_set_start', r.payload ->> 'Appointment Set Date  - start',
      'appointment_set_end', r.payload ->> 'Appointment Set Date  - end',
      'account_research_event_notes', r.payload ->> 'Account Research - Event Notes',
      'action_notes', r.payload ->> 'Action Notes',
      'any_other_contacts', r.payload ->> 'Any Other Contacts',
      'source_file', r.source_file,
      'source_sheet', r.source_sheet,
      'source_row', r.source_row
    ));

    insert into public.campaign_targets (
      organization_id,
      client_id,
      campaign_id,
      company_id,
      status,
      metadata
    ) values (
      v_org_id,
      v_client_id,
      v_campaign_id,
      v_company_id,
      'queued',
      v_target_metadata
    )
    on conflict (campaign_id, company_id) where company_id is not null do update
      set status = excluded.status,
          metadata = public.campaign_targets.metadata || excluded.metadata,
          updated_at = now();

    for v_rank in 1..3 loop
      v_first_name := case v_rank
        when 1 then nullif(trim(coalesce(r.payload ->> '(Primary) 1. Contact First Name', '')), '')
        when 2 then nullif(trim(coalesce(r.payload ->> '2. Contact Firstname', '')), '')
        else nullif(trim(coalesce(r.payload ->> '3. Contact First Name', '')), '')
      end;
      v_last_name := case v_rank
        when 1 then nullif(trim(coalesce(r.payload ->> '(Primary) 1. Contact  Last Name', '')), '')
        when 2 then nullif(trim(coalesce(r.payload ->> '2. Contact Lastname', '')), '')
        else nullif(trim(coalesce(r.payload ->> '3. Contact Last Name', '')), '')
      end;
      v_full_name := nullif(trim(coalesce(v_first_name, '') || ' ' || coalesce(v_last_name, '')), '');
      v_job_title := case v_rank
        when 1 then nullif(trim(coalesce(r.payload ->> '(Primary) 1. Contact Title', '')), '')
        when 2 then nullif(trim(coalesce(r.payload ->> '2. Contact Title', '')), '')
        else nullif(trim(coalesce(r.payload ->> '3. Contact Title', '')), '')
      end;
      v_linkedin := case v_rank
        when 1 then nullif(trim(coalesce(r.payload ->> '(Primary) 1. Personal LinkedIn', r.payload ->> '(Primary) 1. Personal LinkedIn or Additional Links', '')), '')
        when 2 then nullif(trim(coalesce(r.payload ->> '2. Personal LinkedIn', '')), '')
        else nullif(trim(coalesce(r.payload ->> '3. Personal LinkedIn', '')), '')
      end;
      v_mobile := case v_rank
        when 1 then nullif(trim(coalesce(r.payload ->> '(Primary) 1. Direct Dial - Mobile', '')), '')
        when 2 then nullif(trim(coalesce(r.payload ->> '2. Direct Dial - Mobile', '')), '')
        else nullif(trim(coalesce(r.payload ->> '3. Direct Dial - Mobile', '')), '')
      end;
      v_direct_dial := case v_rank
        when 1 then nullif(trim(coalesce(r.payload ->> '(Primary) 1. Direct Dial - Work', '')), '')
        when 2 then nullif(trim(coalesce(r.payload ->> '2. Direct Dial - Work', '')), '')
        else nullif(trim(coalesce(r.payload ->> '3. Direct Dial - Work', '')), '')
      end;
      v_phone := case v_rank
        when 1 then nullif(trim(coalesce(r.payload ->> '(Primary) 1. Direct Dial - Main', '')), '')
        when 2 then nullif(trim(coalesce(r.payload ->> '2. Direct Dial - Main', '')), '')
        else nullif(trim(coalesce(r.payload ->> '3. Direct Dial - Main', '')), '')
      end;
      v_email := case v_rank
        when 1 then nullif(trim(coalesce(r.payload ->> '(Primary) 1. Direct Email - Work', '')), '')
        when 2 then nullif(trim(coalesce(r.payload ->> '2. Direct Email - Work', '')), '')
        else nullif(trim(coalesce(r.payload ->> '3. Direct Email - Work', '')), '')
      end;
      v_location := case v_rank
        when 1 then nullif(trim(coalesce(r.payload ->> '(Primary) Contacts Location', '')), '')
        when 2 then nullif(trim(coalesce(r.payload ->> '2. Contacts Location', '')), '')
        else nullif(trim(coalesce(r.payload ->> '3. Contacts Location', '')), '')
      end;

      if v_full_name is null and v_email is null and v_linkedin is null and v_mobile is null and v_direct_dial is null then
        continue;
      end if;

      v_identity_key := coalesce(
        case when v_email is not null then 'email:' || lower(trim(v_email)) end,
        case when v_linkedin is not null then 'linkedin:' || lower(trim(v_linkedin)) end,
        'person:' || lower(trim(coalesce(v_full_name, 'unknown'))) || '|' || lower(trim(v_company_name))
      );

      select id into v_contact_id
      from public.contacts
      where organization_id = v_org_id
        and normalized_identity_key = v_identity_key
      limit 1;

      if v_contact_id is null then
        insert into public.contacts (
          organization_id,
          client_id,
          company_id,
          contact_name,
          first_name,
          last_name,
          full_name,
          company,
          email,
          manual_email,
          phone,
          mobile,
          manual_mobile,
          direct_dial,
          manual_direct_dial,
          job_title,
          location,
          linkedin_url,
          linkedin_profile_url,
          source_note,
          data_source,
          normalized_identity_key,
          status,
          custom_fields
        ) values (
          v_org_id,
          v_client_id,
          v_company_id,
          v_full_name,
          v_first_name,
          v_last_name,
          v_full_name,
          v_company_name,
          v_email,
          v_email,
          coalesce(v_phone, v_direct_dial, v_mobile),
          v_mobile,
          v_mobile,
          v_direct_dial,
          v_direct_dial,
          v_job_title,
          v_location,
          v_linkedin,
          v_linkedin,
          'Imported from EMSOL campaign workbook',
          'imported_csv',
          v_identity_key,
          'active',
          jsonb_build_object('contact_rank', v_rank, 'source_file', r.source_file, 'source_sheet', r.source_sheet, 'source_row', r.source_row)
        )
        returning id into v_contact_id;
      else
        update public.contacts
        set client_id = coalesce(public.contacts.client_id, v_client_id),
            company_id = v_company_id,
            contact_name = coalesce(public.contacts.contact_name, v_full_name),
            full_name = coalesce(public.contacts.full_name, v_full_name),
            company = v_company_name,
            first_name = coalesce(public.contacts.first_name, v_first_name),
            last_name = coalesce(public.contacts.last_name, v_last_name),
            email = coalesce(public.contacts.email, v_email),
            manual_email = coalesce(public.contacts.manual_email, v_email),
            phone = coalesce(public.contacts.phone, coalesce(v_phone, v_direct_dial, v_mobile)),
            mobile = coalesce(public.contacts.mobile, v_mobile),
            manual_mobile = coalesce(public.contacts.manual_mobile, v_mobile),
            direct_dial = coalesce(public.contacts.direct_dial, v_direct_dial),
            manual_direct_dial = coalesce(public.contacts.manual_direct_dial, v_direct_dial),
            job_title = coalesce(public.contacts.job_title, v_job_title),
            location = coalesce(public.contacts.location, v_location),
            linkedin_url = coalesce(public.contacts.linkedin_url, v_linkedin),
            linkedin_profile_url = coalesce(public.contacts.linkedin_profile_url, v_linkedin),
            custom_fields = public.contacts.custom_fields || jsonb_build_object('contact_rank', v_rank, 'source_file', r.source_file, 'source_sheet', r.source_sheet, 'source_row', r.source_row),
            updated_at = now()
        where id = v_contact_id;
      end if;

      insert into public.campaign_targets (
        organization_id,
        client_id,
        campaign_id,
        company_id,
        contact_id,
        status,
        metadata
      ) values (
        v_org_id,
        v_client_id,
        v_campaign_id,
        null,
        v_contact_id,
        'queued',
        v_target_metadata || jsonb_build_object('contact_rank', v_rank, 'company_id', v_company_id)
      )
      on conflict (campaign_id, contact_id) where contact_id is not null do update
        set status = excluded.status,
            metadata = public.campaign_targets.metadata || excluded.metadata,
            updated_at = now();
    end loop;
  end loop;
end $$;
`;

fs.writeFileSync(outputPath, sql);
console.log(`Wrote ${rows.length} workbook rows to ${path.relative(rootDir, outputPath)}`);
