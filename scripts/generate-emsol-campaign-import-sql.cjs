const fs = require("fs");
const path = require("path");
const XLSX = require("xlsx");

const rootDir = path.resolve(__dirname, "..");
const campaignsDir = path.join(rootDir, "sampleCampaigns");
const outputPath = path.join(rootDir, "supabase", "emsol_campaign_import.sql");

const files = [
  {
    file: "NHS Trust - Contacts - Last view used.xlsx",
    campaignName: "NHS Trust - Contacts",
    listName: "NHS Trust - Contacts Target Accounts",
  },
  {
    file: "NHS Webinar - Account Priority .xlsx",
    campaignName: "NHS Webinar",
    listName: "NHS Webinar Target Accounts",
  },
  {
    file: "Construction - Contacts - Last view used.xlsx",
    campaignName: "Construction - Contacts",
    listName: "Construction - Contacts Target Accounts",
  },
];

function clean(value) {
  if (value === null || value === undefined) return null;
  const text = String(value).replace(/\u00a0/g, " ").trim();
  return text.length ? text : null;
}

function normalizeRow(row) {
  return Object.fromEntries(
    Object.entries(row)
      .map(([key, value]) => [clean(key), clean(value)])
      .filter(([key, value]) => key && value !== null),
  );
}

function sqlString(value) {
  return value === null || value === undefined ? "null" : `'${String(value).replace(/'/g, "''")}'`;
}

function sqlJson(value) {
  return `'${JSON.stringify(value).replace(/'/g, "''")}'::jsonb`;
}

const rows = [];

for (const config of files) {
  const workbookPath = path.join(campaignsDir, config.file);
  const workbook = XLSX.readFile(workbookPath, { cellDates: false });

  for (const sheetName of workbook.SheetNames) {
    const sheet = workbook.Sheets[sheetName];
    const sheetRows = XLSX.utils.sheet_to_json(sheet, { defval: null, raw: false });

    sheetRows.forEach((row, index) => {
      const payload = normalizeRow(row);
      if (!payload["Organisation Name"]) return;

      rows.push({
        campaignName: config.campaignName,
        listName: config.listName,
        sourceFile: config.file,
        sourceSheet: sheetName,
        sourceRow: index + 2,
        payload,
      });
    });
  }
}

const rowsJson = rows.map((row) => ({
  campaign_name: row.campaignName,
  list_name: row.listName,
  source_file: row.sourceFile,
  source_sheet: row.sourceSheet,
  source_row: row.sourceRow,
  payload: row.payload,
}));

